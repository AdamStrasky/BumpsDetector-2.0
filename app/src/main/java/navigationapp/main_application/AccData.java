package navigationapp.main_application;

public class AccData {

    private float x = 0;
    private float y = 0;
    private float z = 0;

    public AccData(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getZ() {
        return z;
    }
}
